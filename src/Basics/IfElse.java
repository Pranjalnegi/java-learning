package Basics;

public class IfElse {

	public static void main(String[] args) {
		int salary=50000;
		if (salary>50000){
			System.out.println("My salary is goood"+salary);
		}
		else if (salary<50000) {
			System.out.println("My salary is very low "+salary);
		}
		else {
			System.out.println("My salary is Ok as it is  "+salary);
		}
	}

}
